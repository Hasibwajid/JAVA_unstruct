package OOPS_FreeCodeCamp.Advance.Collections;

public class student {
    private String Name;
    private int Id;


    public student(String name, int id) {
        Name = name;
        Id = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public int getId() {
        return Id;
    }

    public void setId(int id) {
        Id = id;
    }
}